<?php
function month_count($a){
	if($a==1){
		return "Januari";
	}
	if($a==2){
		return "Februari";
	}
	if($a==3){
		return "Maret";
	}
	if($a==4){
		return "April";
	}
	if($a==5){
		return "Mei";
	}
	if($a==6){
		return "Juni";
	}
	if($a==7){
		return "July";
	}
	if($a==8){
		return "Agustus";
	}
	if($a==9){
		return "September";
	}
	if($a==10){
		return "Oktober";
	}
	if($a==11){
		return "November";
	}
	if($a==12){
		return "Desember";
	}
}
?>